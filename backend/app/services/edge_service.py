# ── Provisioning ────────────────────────────────────────────────


async def provision_agent(
    db: AsyncIOMotorDatabase,
    org_id: str,
    store_id: str,
    name: str,
) -> dict:
    """Generate edge agent token and create agent record."""
    # Verify store exists
    store = await db.stores.find_one({**org_query(org_id), "id": store_id, "is_active": True})
    if not store:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Store not found")

    agent_id = str(uuid.uuid4())
    now = datetime.now(timezone.utc)

    # Generate edge JWT
    payload = {
        "sub": agent_id,
        "org_id": org_id,
        "store_id": store_id,
        "type": "edge_agent",
        "iat": now,
        "exp": now + timedelta(days=settings.EDGE_TOKEN_EXPIRE_DAYS),
    }
    token = jwt.encode(payload, settings.EDGE_SECRET_KEY, algorithm="HS256")
    token_hash = hash_password(token)

    # Generate edge API key (for authenticating cloud→edge requests)
    import hashlib
    edge_api_key = hashlib.sha256(token.encode()).hexdigest()[:32]

    # Generate docker-compose template
    docker_compose = _generate_docker_compose(agent_id, store.get("name", name), org_id, store_id, edge_api_key)

    # Auto-create Cloudflare tunnel for edge-to-cloud connectivity
    tunnel_data = None
    if settings.CF_API_TOKEN and settings.CF_ACCOUNT_ID:
        try:
            from app.services.cloudflare_service import create_tunnel
            tunnel_data = await create_tunnel(agent_id, store.get("name", name))
        except Exception as e:
            log.warning("Cloudflare tunnel creation failed (non-critical): %s", e)

    agent_doc = {
        "id": agent_id,
        "org_id": org_id,
        "store_id": store_id,
        "name": name,
        "token_hash": token_hash,
        "edge_api_key": edge_api_key,
        "agent_version": None,
        "current_model_version": None,
        "status": "offline",
        "last_heartbeat": None,
        "cpu_percent": None,
        "ram_percent": None,
        "disk_percent": None,
        "gpu_percent": None,
        "inference_fps": None,
        "buffer_frames": 0,
        "buffer_size_mb": 0.0,
        "tunnel_status": None,
        "tunnel_latency_ms": None,
        "camera_count": 0,
        "cf_tunnel_id": tunnel_data["tunnel_id"] if tunnel_data else None,
        "cf_tunnel_token": tunnel_data["tunnel_token"] if tunnel_data else None,
        "cf_dns_record_id": tunnel_data["dns_record_id"] if tunnel_data else None,
        "tunnel_hostname": tunnel_data["hostname"] if tunnel_data else None,
        "created_at": now,
    }
    await db.edge_agents.insert_one(agent_doc)

    return {
        "agent_id": agent_id,
        "token": token,
        "docker_compose": docker_compose,
        "tunnel_token": tunnel_data["tunnel_token"] if tunnel_data else None,
        "tunnel_hostname": tunnel_data["hostname"] if tunnel_data else None,
    }


