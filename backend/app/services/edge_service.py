async def list_agents(
    db: AsyncIOMotorDatabase,
    org_id: str,
    store_id: str | None = None,
    limit: int = 20,
    offset: int = 0,
) -> tuple[list[dict], int]:
    query: dict = org_query(org_id)
    if store_id:
        query["store_id"] = store_id

    total = await db.edge_agents.count_documents(query)
