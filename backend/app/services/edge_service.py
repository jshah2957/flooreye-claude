async def delete_agent(db: AsyncIOMotorDatabase, agent_id: str, org_id: str) -> None:
    # Fetch agent before deletion for tunnel cleanup
    agent = await db.edge_agents.find_one({**org_query(org_id), "id": agent_id})
    if not agent:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Agent not found")

    # Clean up Cloudflare tunnel if one was auto-created
    if agent.get("cf_tunnel_id") and settings.CF_API_TOKEN:
        try:
            from app.services.cloudflare_service import delete_tunnel
            await delete_tunnel(agent["cf_tunnel_id"], agent.get("cf_dns_record_id"))
        except Exception as e:
            log.warning("Cloudflare tunnel cleanup failed: %s", e)

    await db.edge_agents.delete_one({"id": agent_id, "org_id": org_id})
    await db.edge_commands.delete_many({"agent_id": agent_id})
    # Unlink cameras — don't delete them, just clear the agent reference
    await db.cameras.update_many(
        {"edge_agent_id": agent_id, "org_id": org_id},
        {"$set": {"edge_agent_id": None, "config_status": "unlinked"}},
    )


async def update_agent(
    db: AsyncIOMotorDatabase, agent_id: str, org_id: str, name: str | None = None
) -> dict:
    """Update edge agent properties."""
    agent = await db.edge_agents.find_one({**org_query(org_id), "id": agent_id})
    if not agent:
        raise HTTPException(status_code=status.HTTP_404_NOT_FOUND, detail="Agent not found")
    updates = {}
    if name is not None:
        updates["name"] = name
    if updates:
        updates["updated_at"] = datetime.now(timezone.utc)
        await db.edge_agents.update_one({"id": agent_id}, {"$set": updates})
        agent.update(updates)
    agent.pop("_id", None)
    return agent


async def mark_stale_agents_offline(
    db: AsyncIOMotorDatabase,
    stale_threshold_minutes: int | None = None,
) -> int:
    """Mark agents as offline if their last heartbeat exceeds the threshold.

    Returns the number of agents marked offline. Emits a system log for each.
    Called periodically by the health worker.
    """
    if stale_threshold_minutes is None:
        stale_threshold_minutes = settings.EDGE_AGENT_STALE_THRESHOLD_MINUTES
    cutoff = datetime.now(timezone.utc) - timedelta(minutes=stale_threshold_minutes)
    query = {
        "status": "online",
        "last_heartbeat": {"$lt": cutoff},
    }
    stale_agents = await db.edge_agents.find(query).to_list(length=1000)
    count = 0
    for agent in stale_agents:
        await db.edge_agents.update_one(
            {"id": agent["id"]},
            {"$set": {"status": "offline"}},
        )
        org_id = agent.get("org_id", "")
        await emit_system_log(
            db, org_id, "error", "edge", "Edge agent offline",
            {"agent_id": agent["id"], "last_heartbeat": agent.get("last_heartbeat", "").isoformat() if hasattr(agent.get("last_heartbeat", ""), "isoformat") else str(agent.get("last_heartbeat", ""))},
        )
        count += 1
    return count


async def check_config_staleness(db: AsyncIOMotorDatabase, agent_id: str, camera_configs: dict) -> list:
    """Compare edge-reported config versions against cloud versions.
    Returns list of cameras that need config updates.
    """
    if not camera_configs:
        return []

    camera_ids = list(camera_configs.keys())
    cameras = await db.cameras.find(
        {"id": {"$in": camera_ids}, "edge_agent_id": agent_id},
        {"id": 1, "config_version": 1, "_id": 0}
    ).to_list(500)

    stale = []
    for cam in cameras:
        cloud_ver = cam.get("config_version", 0)
        edge_info = camera_configs.get(cam["id"], {})
        edge_ver = edge_info.get("config_version", 0) if isinstance(edge_info, dict) else 0
        if cloud_ver > edge_ver:
            stale.append({
                "camera_id": cam["id"],
                "cloud_version": cloud_ver,
                "edge_version": edge_ver,
            })
    return stale
