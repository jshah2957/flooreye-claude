def _generate_gpu_override() -> str:
    """Generate docker-compose.gpu.yml for GPU-enabled inference."""
    return """# GPU override — used automatically by install.sh when NVIDIA GPU is detected
services:
  inference-server:
    deploy:
      resources:
        reservations:
          devices:
            - driver: nvidia
              count: 1
              capabilities: [gpu]
"""


def _generate_install_script() -> str:
    """Generate install.sh — the one-command installer."""
    return """#!/bin/bash
set -e
echo "=== FloorEye Edge Agent Setup ==="
echo ""

# Create required directories
mkdir -p models data/buffer data/clips data/frames data/config data/redis
echo "✓ Directories created"

# Create default go2rtc config if missing
if [ ! -f data/config/go2rtc.yaml ]; then
    echo "streams: {}" > data/config/go2rtc.yaml
    echo "✓ go2rtc config created"
fi

# Check for model
if [ -f models/model.onnx ]; then
    echo "✓ AI model found"
elif ls models/*.onnx 1>/dev/null 2>&1; then
    echo "✓ AI model found"
else
    echo "⚠ No AI model found — agent will download from cloud on first boot"
fi

# Detect GPU
if command -v nvidia-smi &>/dev/null && nvidia-smi &>/dev/null; then
    echo "✓ NVIDIA GPU detected — using GPU inference"
    COMPOSE_CMD="docker compose -f docker-compose.yml -f docker-compose.gpu.yml up -d --build"
else
    echo "• No GPU detected — using CPU inference (slower but works)"
    COMPOSE_CMD="docker compose up -d --build"
fi

# Start containers
echo ""
echo "Building and starting containers (this may take a few minutes on first run)..."
$COMPOSE_CMD

# Wait for health
echo ""
echo "Waiting for agent to start..."
for i in $(seq 1 90); do
    if curl -sf http://localhost:8091/api/health >/dev/null 2>&1; then
        echo ""
        echo "============================================"
        echo "  ✓ FloorEye Edge Agent is ONLINE"
        echo "============================================"
        echo ""
        echo "  Web UI:    http://localhost:8090"
        echo "  Health:    http://localhost:8091/api/health"
        echo ""
        echo "  Next: Go to your cloud dashboard and add cameras."
        echo ""
        exit 0
    fi
    sleep 2
    printf "."
done
echo ""
echo "⚠ Agent did not start within 3 minutes."
echo "  Check logs: docker compose logs -f"
echo "  Common issues:"
echo "    - Docker not running: sudo systemctl start docker"
echo "    - Port conflict: check if 8080/8090/8091 are in use"
exit 1
"""


