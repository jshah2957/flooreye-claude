# Path to edge-agent source code (relative to backend/)
_EDGE_AGENT_ROOT = Path(__file__).resolve().parent.parent.parent.parent / "edge-agent"


def _generate_env(agent: dict, token: str, tunnel_token: str | None = None) -> str:
    """Generate .env file with all required variables pre-filled."""
    lines = [
        f"EDGE_TOKEN={token}",
        f"AGENT_ID={agent['id']}",
        f"ORG_ID={agent['org_id']}",
        f"STORE_ID={agent['store_id']}",
        f"BACKEND_URL={settings.BACKEND_URL}",
        f"EDGE_API_KEY={agent.get('edge_api_key', '')}",
    ]
    if tunnel_token:
        lines.append(f"TUNNEL_TOKEN={tunnel_token}")
    else:
        lines.append("# TUNNEL_TOKEN=  # Optional: add Cloudflare tunnel token for remote management")
    return "\n".join(lines) + "\n"


